
package fr.sym;

/**
 * Un thème est un element rattaché à un troncon.
 * 
 * @author Johann Sorel
 */
public class Theme {
    
    private String name;

    public Theme() {
    }

    public Theme(String name) {
        this.name = name;
    }
    
    public String getName(){
        return name;
    }
        
    
}
